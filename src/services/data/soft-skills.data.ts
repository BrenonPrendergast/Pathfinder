// Helper functions for skill tree logic
export const getSoftSkillById = (id: string): SoftSkill | undefined => {
  return SOFT_SKILLS.find(skill => skill.id === id);
};

export const getSoftSkillsByCategory = (category: SkillCategory): SoftSkill[] => {
  return SOFT_SKILLS.filter(skill => skill.category === category);
};

export const getDefaultUnlockedSkills = (): SoftSkill[] => {
  return SOFT_SKILLS.filter(skill => skill.defaultUnlocked);
};

export const getSkillPrerequisites = (skillId: string): string[] => {
  const skill = getSoftSkillById(skillId);
  return skill?.prerequisites || [];
};

export const isSkillUnlockable = (skillId: string, unlockedSkillIds: string[]): boolean => {
  const prerequisites = getSkillPrerequisites(skillId);
  return prerequisites.every(prereqId => unlockedSkillIds.includes(prereqId));
};

